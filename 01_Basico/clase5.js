/* Clase 5 - Arreglos */
// Es una coleccion de datos relacionados
// Se almacenan en una variable 

var premierLeague = ["Chelsea", "Manchester United", "Liverpool", "Arsenal"];

// Presentan un indexado iniciando con 0
console.log(premierLeague[3]);

// Otra forma de hacer un arreglo es usando la notación de objetos

var la_liga = new Array("Barcelona", "Real Madrid", "Atletico Madrid");


// Para saber si un arreglo tiene información utilizamos el metodo
la_liga.length;

// Cambiar un valor de un arreglo
la_liga[1] = "Valencia";

// Ordenar de forma invertida un arreglo
la_liga.reverse;