/* Clase 3 - Objetos (JSON) */
// Utilizaremos console.log() para imprimir cosas en la consola de JS, 
// puedes tambien utilizar alert() pero abre una ventana emergente

// Los objetos nos permitiran hacer muchas asignaciones a una sola variable.

var auto = {
    nombre: 'Mustang',
    motor: 5.5,
    color: 'azul',
    clasico: true,
    anio: 1965,
};

// Para acceder al valor asignado utilizamos el dot notation
console.log(auto);
console.log(auto.nombre);
console.log(auto.anio);