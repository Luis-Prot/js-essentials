/* Clase 15 - Operadores */
// Existen muchos operadores los más básicos son los matemáticos

var numero1 = 10;
var numero2 = 20;

// Suma
console.log(numero1 + numero2);
// Resta
console.log(numero1 - numero2);
// Multiplicación
console.log(numero1 * numero2);
// División
console.log(numero1 / numero2);

// Incrementos y decrementos
numero1--;
console.log(numero1); // Lo cambia a 9

numero1++;
console.log(numero1); // Lo cambia a 10


numero3 = 30;
numero3 += 20;
console.log(numero3);

numero3 -= 10;
console.log(numero3);

numero3 *= 2;
console.log(numero3);

numero3 /= 4;
console.log(numero3);